package com.practice_19_04;

public class SimpleInterest {

	public static void main(String[] args) {
		// Write a program to calculate Simple Interest
		float interest;
		
		float p = 2000.90f;
		
		float r = 2.5f;
		
		float n = 2.4f;
		
		interest = (p*r*n)/100;
		
		System.out.println("Calculated interest = " + interest);

	}

}
